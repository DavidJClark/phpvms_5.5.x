<h3>Help</h3>
<p>These are the basic settings for your phpVMS install.</p>

<p><strong>Virtual Airline Name</strong> - 
This name will show up in the title bar. This is your airline name, 
such as "phpVMS Airline Group". This is independent from the airlines
you have setup</p>

<p><strong>Webmaster Email</strong> - 
This is the email address which the system will send notifications to,
and where email's sent to Pilots will appear to be from.</p>

<p><strong>Date Format</strong> - 
You can format the date/time to show however you want. The format
is in the <a href="http://us2.php.net/date" target="_new">PHP Date Format</a>.</p>

<p><strong>Current Skin</strong> - 
Choose the skin to use on the front-end of your website</p>

<p><strong>Default User Group</strong> - 
This is the default group newly registered pilots will be placed in.</p>
